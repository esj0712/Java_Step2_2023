package chapter14.wrapper;

public class ValueCompareExample {

	public static void main(String[] args) {
		// 오토박싱 상황에서는 1byte초과시 비교판단이 안됨
		System.out.println("[ -128 ~ 128 초과값일 경우 ]"); // 1btye = 8bit
		Integer obj1 = 300; // 4byte => 32bit
		Integer obj2 = 300; // 300은 객체를 뜻함

		// 주소를 비교 (범위내에 없을 경우)
		System.out.println("==결과 : " + (obj1 == obj2)); // 외부 비교
		System.out.println("==언박싱한 후 결과 : " + (obj1.intValue() == obj2.intValue())); // 내부 비교

		System.out.println("========================");

		System.out.println("[ -128 ~ 128 범위값일 경우]"); // 1btye=8bit
		Integer obj3 = 100; // 4byte => 32bit
		Integer obj4 = 100; // 100은 객체를 뜻함

		// 주소를 비교 (범위내에 있을 경우)
		System.out.println("==결과 : " + (obj3 == obj4)); // 외부 비교
		System.out.println("==언박싱한 후 결과 : " + (obj3.intValue() == obj4.intValue())); // 내부 비교
		System.out.println("==equals 결과 : " + obj3.equals(obj4)); 
	}

}
